class DataTypes {
    public static void main(String[] args) {

        int age = 20;
        float marks = 85.5f;
        char grade = 'A';
        boolean result = true;

        System.out.println("Age: " + age);
        System.out.println("Marks: " + marks);
        System.out.println("Grade: " + grade);
        System.out.println("Result: " + result);
    }
}
